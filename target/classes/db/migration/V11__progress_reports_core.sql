-- ─── 1. Extend admin_users to allow TEACHER role ──────────────
-- admin_users is the single login table for the whole ERP.
-- We link a TEACHER-role row to its teachers row via teacher_id.
ALTER TABLE admin_users
    ADD COLUMN IF NOT EXISTS teacher_id UUID REFERENCES teachers(id) ON DELETE CASCADE;

CREATE UNIQUE INDEX IF NOT EXISTS uq_admin_users_teacher_id
    ON admin_users(teacher_id) WHERE teacher_id IS NOT NULL;

-- Drop and recreate the role check if one exists as a CHECK constraint.
-- (Role is stored as VARCHAR with app-level enum validation, not a DB CHECK,
--  per V5's comment — so no constraint to alter. TEACHER is just a new
--  valid string value going forward.)

-- ─── 2. Reporting Cycles ───────────────────────────────────────
CREATE TABLE IF NOT EXISTS reporting_cycles (
    id           UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name         VARCHAR(60)  NOT NULL,           -- e.g. "Cycle 01 - Jul 2026"
    start_date   DATE         NOT NULL,
    end_date     DATE         NOT NULL,
    session_id   UUID REFERENCES academic_sessions(id),
    status       VARCHAR(20)  NOT NULL DEFAULT 'OPEN',  -- OPEN, CLOSED, ARCHIVED
    created_by   UUID REFERENCES admin_users(id),
    created_at   TIMESTAMP    NOT NULL DEFAULT NOW(),
    updated_at   TIMESTAMP    NOT NULL DEFAULT NOW(),
    CHECK (end_date >= start_date),
    CHECK (status IN ('OPEN', 'CLOSED', 'ARCHIVED'))
);

CREATE INDEX IF NOT EXISTS idx_reporting_cycles_status ON reporting_cycles(status);
CREATE INDEX IF NOT EXISTS idx_reporting_cycles_session ON reporting_cycles(session_id);

-- ─── 3. Class → Subject mapping (string-keyed) ─────────────────
-- The live app organizes students by free-text current_class/section
-- (see students.current_class), NOT the dormant classes/class_subjects
-- tables, which have no API and no data. This module follows the live
-- model: class_subject_map keys off the same class name strings used
-- everywhere else in the app (e.g. "Class V").
CREATE TABLE IF NOT EXISTS class_subject_map (
    id           UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    class_name   VARCHAR(20) NOT NULL,   -- matches students.current_class exactly
    subject_id   UUID NOT NULL REFERENCES subjects(id) ON DELETE CASCADE,
    created_at   TIMESTAMP NOT NULL DEFAULT NOW(),
    UNIQUE(class_name, subject_id)
);

CREATE INDEX IF NOT EXISTS idx_class_subject_map_class ON class_subject_map(class_name);

-- Seed sensible defaults: every existing class gets the 6 core subjects
-- referenced in the spec, wherever those subject codes already exist.
-- Admins can adjust per class afterwards (e.g. remove Computer for Nursery).
INSERT INTO class_subject_map (class_name, subject_id)
SELECT c.class_name, s.id
FROM (VALUES
    ('Nursery'),('KG I'),('KG II'),
    ('Class I'),('Class II'),('Class III'),('Class IV'),('Class V'),
    ('Class VI'),('Class VII'),('Class VIII'),('Class IX'),('Class X')
) AS c(class_name)
CROSS JOIN subjects s
WHERE s.code IN ('ENG','HIN','MAT','SCI','SST','CS')
ON CONFLICT (class_name, subject_id) DO NOTHING;

-- ─── 4. Student Assessments (one row per student per cycle) ──
CREATE TABLE IF NOT EXISTS student_assessments (
    id                UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    cycle_id          UUID NOT NULL REFERENCES reporting_cycles(id) ON DELETE CASCADE,
    student_id        UUID NOT NULL REFERENCES students(id) ON DELETE CASCADE,

    -- Denormalized snapshot of class/section at the time of entry.
    -- A student could be promoted/transferred between cycles, so we
    -- freeze what class this particular report is FOR, not a live FK.
    class_name        VARCHAR(20) NOT NULL,
    section           VARCHAR(5),

    -- Attendance
    working_days      INT,
    present_days      INT,
    attendance_pct    NUMERIC(5,2),  -- generated on save by service layer

    -- Behaviour & participation (1-5 scale)
    discipline_score        SMALLINT CHECK (discipline_score BETWEEN 1 AND 5),
    homework_score           SMALLINT CHECK (homework_score BETWEEN 1 AND 5),
    participation_score      SMALLINT CHECK (participation_score BETWEEN 1 AND 5),
    punctuality_score        SMALLINT CHECK (punctuality_score BETWEEN 1 AND 5),
    communication_score      SMALLINT CHECK (communication_score BETWEEN 1 AND 5),
    teamwork_score            SMALLINT CHECK (teamwork_score BETWEEN 1 AND 5),
    behaviour_overall        NUMERIC(3,2),  -- avg of the above, generated by service

    -- Remarks
    teacher_remarks   TEXT,
    principal_remarks TEXT,

    -- Overall computed performance label, e.g. OUTSTANDING/EXCELLENT/GOOD/AVERAGE/NEEDS_IMPROVEMENT
    overall_performance VARCHAR(30),

    status            VARCHAR(20) NOT NULL DEFAULT 'DRAFT', -- DRAFT, SUBMITTED, LOCKED
    submitted_by      UUID REFERENCES admin_users(id),
    submitted_at      TIMESTAMP,
    created_at        TIMESTAMP NOT NULL DEFAULT NOW(),
    updated_at        TIMESTAMP NOT NULL DEFAULT NOW(),

    UNIQUE(cycle_id, student_id),
    CHECK (status IN ('DRAFT', 'SUBMITTED', 'LOCKED'))
);

CREATE INDEX IF NOT EXISTS idx_assessments_cycle ON student_assessments(cycle_id);
CREATE INDEX IF NOT EXISTS idx_assessments_student ON student_assessments(student_id);
CREATE INDEX IF NOT EXISTS idx_assessments_class ON student_assessments(class_name, section);
CREATE INDEX IF NOT EXISTS idx_assessments_status ON student_assessments(status);

-- ─── 5. Subject-wise performance per assessment ────────────────
-- One row per (assessment, subject). Supports either marks OR rating
-- mode (school-wide setting, enforced at the application layer).
CREATE TABLE IF NOT EXISTS assessment_subjects (
    id             UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    assessment_id  UUID NOT NULL REFERENCES student_assessments(id) ON DELETE CASCADE,
    subject_id     UUID NOT NULL REFERENCES subjects(id),
    marks          NUMERIC(5,2),   -- used when school is in MARKS mode
    rating         VARCHAR(20),    -- used when school is in RATING mode: OUTSTANDING/EXCELLENT/GOOD/AVERAGE/NEEDS_IMPROVEMENT
    created_at     TIMESTAMP NOT NULL DEFAULT NOW(),
    updated_at     TIMESTAMP NOT NULL DEFAULT NOW(),
    UNIQUE(assessment_id, subject_id),
    CHECK (rating IS NULL OR rating IN ('OUTSTANDING','EXCELLENT','GOOD','AVERAGE','NEEDS_IMPROVEMENT'))
);

CREATE INDEX IF NOT EXISTS idx_assessment_subjects_assessment ON assessment_subjects(assessment_id);

-- ─── 6. Generated Progress Reports (PDF artifacts) ─────────────
CREATE TABLE IF NOT EXISTS progress_reports (
    id              UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    assessment_id   UUID NOT NULL REFERENCES student_assessments(id) ON DELETE CASCADE,
    cycle_id        UUID NOT NULL REFERENCES reporting_cycles(id),
    student_id      UUID NOT NULL REFERENCES students(id),
    pdf_url         VARCHAR(500),         -- Cloudinary URL once generated
    qr_code         VARCHAR(100) NOT NULL, -- verification token embedded in PDF QR
    generated_at    TIMESTAMP NOT NULL DEFAULT NOW(),
    generated_by    UUID REFERENCES admin_users(id),
    UNIQUE(assessment_id),
    UNIQUE(qr_code)
);

CREATE INDEX IF NOT EXISTS idx_progress_reports_student ON progress_reports(student_id);
CREATE INDEX IF NOT EXISTS idx_progress_reports_cycle ON progress_reports(cycle_id);

-- ─── 7. Communication Logs (email + WhatsApp, unified) ─────────
CREATE TABLE IF NOT EXISTS communication_logs (
    id              UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    report_id       UUID REFERENCES progress_reports(id) ON DELETE CASCADE,
    student_id      UUID NOT NULL REFERENCES students(id),
    channel         VARCHAR(20)  NOT NULL,  -- EMAIL, WHATSAPP
    recipient       VARCHAR(150) NOT NULL,  -- email address or phone number
    subject         VARCHAR(200),           -- only for EMAIL
    message_preview VARCHAR(500),
    status          VARCHAR(20)  NOT NULL DEFAULT 'PENDING', -- PENDING, SENT, DELIVERED, FAILED
    error_message   TEXT,
    provider_ref    VARCHAR(150),  -- SMTP message-id or WhatsApp message id
    sent_at         TIMESTAMP,
    created_at      TIMESTAMP NOT NULL DEFAULT NOW(),

    CHECK (channel IN ('EMAIL', 'WHATSAPP')),
    CHECK (status IN ('PENDING', 'SENT', 'DELIVERED', 'FAILED'))
);

CREATE INDEX IF NOT EXISTS idx_comm_logs_report ON communication_logs(report_id);
CREATE INDEX IF NOT EXISTS idx_comm_logs_student ON communication_logs(student_id);
CREATE INDEX IF NOT EXISTS idx_comm_logs_status ON communication_logs(status);
CREATE INDEX IF NOT EXISTS idx_comm_logs_channel ON communication_logs(channel);

-- ─── 8. Teacher → Class/Section assignments (string-keyed) ────
-- Mirrors teacher_assignments' intent but keyed on the same
-- class_name/section strings the live app actually uses (see
-- class_subject_map above for why). A teacher can be assigned to
-- teach a subject in a class-section, or be the class's homeroom/
-- "class teacher" (subject_id NULL) for cycle-level submission rights.
CREATE TABLE IF NOT EXISTS teacher_class_assignments (
    id           UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    teacher_id   UUID NOT NULL REFERENCES teachers(id) ON DELETE CASCADE,
    class_name   VARCHAR(20) NOT NULL,
    section      VARCHAR(5),
    subject_id   UUID REFERENCES subjects(id) ON DELETE CASCADE,  -- NULL = class teacher (all subjects)
    is_class_teacher BOOLEAN NOT NULL DEFAULT FALSE,
    created_at   TIMESTAMP NOT NULL DEFAULT NOW(),
    UNIQUE(teacher_id, class_name, section, subject_id)
);

CREATE INDEX IF NOT EXISTS idx_tca_teacher ON teacher_class_assignments(teacher_id);
CREATE INDEX IF NOT EXISTS idx_tca_class ON teacher_class_assignments(class_name, section);

-- ─── 9. School-wide assessment mode setting ────────────────────
-- Single-row config table; school-wide MARKS vs RATING toggle.
CREATE TABLE IF NOT EXISTS assessment_settings (
    id                  SMALLINT PRIMARY KEY DEFAULT 1,
    performance_mode    VARCHAR(10) NOT NULL DEFAULT 'RATING', -- MARKS or RATING
    attendance_alert_pct NUMERIC(5,2) NOT NULL DEFAULT 75.00,
    updated_at          TIMESTAMP NOT NULL DEFAULT NOW(),
    CHECK (id = 1),
    CHECK (performance_mode IN ('MARKS', 'RATING'))
);

INSERT INTO assessment_settings (id, performance_mode, attendance_alert_pct)
VALUES (1, 'RATING', 75.00)
ON CONFLICT (id) DO NOTHING;
